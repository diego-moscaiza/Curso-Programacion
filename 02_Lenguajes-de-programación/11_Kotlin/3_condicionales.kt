package 1_Sintaxis_Kotlin

fun main() {
    //Condicionales - Control de flujo
    
    // If, else - else if

    myInt = 11
    myString = "Adios"

    if (myInt == 11 && myString == "Hola") {
        println("El valor es 11")
    } else if (myInt == 10 || myString == "Hola") {
        println("El valor es 10")
    } else {
        println("El valor no es 10 ni 11")
    }

}